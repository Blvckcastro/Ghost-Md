Function({

	on: 'text',

	fromMe: false

}, async (message, match) => {

	if (!await isChatBot(message)) return

	if (!message.reply_message) return

	if (!message.reply_message.data.key.fromMe) return

	await message.reply(await chatbot(message))

})
